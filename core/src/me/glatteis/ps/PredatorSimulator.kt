class PredatorSimulator : Game() {

    //Technical bits with LibGDX

    override fun create() {
        screen = PredatorScreen()
    }

    override fun render() {
        Gdx.gl.glClearColor(0f, 0f, 0f, 1f)
        Gdx.gl.glClear(GL20.GL_COLOR_BUFFER_BIT)
        screen.render(Gdx.graphics.deltaTime)
    }

    override fun dispose() {
        screen.dispose()
    }
}
